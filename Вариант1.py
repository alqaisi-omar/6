# Вариант 1
print("1. Дан одномерный массив, состоящий из N целочисленных элементов. Ввести массив с клавиатуры. Найти максимальный элемент. Вывести массив на экран в обратном порядке.")
n = int(input("Введите длину массива: "))
array = []

# Ввод массива
for i in range(n):
    array.append(int(input(f"Введите {i + 1}-й элемент: ")))

# Находим максимальный элемент
max_element = max(array)

# Выводим массив на экран в обратном порядке
print("Массив в обратном порядке:")
for i in range(n - 1, -1, -1):
    print(array[i], end=" ")

print("\nМаксимальный элемент:", max_element)

# Вторая часть задачи
print("\n2. В массиве действительных чисел все нулевые элементы заменить на среднее арифметическое всех элементов массива.")

# Находим среднее арифметическое всех элементов
mean_value = sum(array) / n

# Заменяем нулевые элементы на среднее арифметическое
for i in range(n):
    if array[i] == 0:
        array[i] = mean_value

print("Исходный массив с заменой нулей:", array)
