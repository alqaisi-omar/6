print("1. Определите, есть ли в списке повторяющиеся элементы, если да, то вывести на экран это значение, иначе сообщение об их отсутствии.")
array = [int(input(f"Введите {i + 1}-й элемент: ")) for i in range(10)]

# Проверяем наличие повторяющихся элементов
found_duplicate = False
for i in range(len(array)):
    if array.count(array[i]) > 1:
        print("Найден повторяющийся элемент:", array[i])
        found_duplicate = True
        break

if not found_duplicate:
    print("Повторяющихся элементов нет.")

print("\n2. Дан одномерный массив из 15 элементов. Элементам массива меньше 10 присвоить нулевые значения, а элементам больше 20 присвоить 1. Вывести на экран монитора первоначальный и преобразованный массивы в строчку.")
array = [int(input(f"Введите {i + 1}-й элемент: ")) for i in range(15)]

# Преобразовываем массив
for i in range(len(array)):
    if array[i] < 10:
        array[i] = 0
    elif array[i] > 20:
        array[i] = 1

print("Первоначальный массив:", array)
print("Преобразованный массив:", array)
