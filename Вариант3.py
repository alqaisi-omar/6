print("1. В одномерном числовом массиве D длиной n вычислить сумму элементов с нечетными индексами. Вывести на экран массив D и полученную сумму.")
n = int(input("Введите длину массива D: "))
D = []

# Ввод массива D
for i in range(n):
    D.append(int(input(f"Введите {i + 1}-й элемент: ")))

# Вычисляем сумму элементов с нечетными индексами
sum_odd_indices = sum(D[1::2])

print("Массив D:", D)
print("Сумма элементов с нечетными индексами:", sum_odd_indices)

print("\n2. Дан одномерный массив из 8 элементов. Заменить все элементы массива меньшие 15 их удвоенными значениями. Вывести на экран монитора преобразованный массив.")
array = [int(input(f"Введите {i + 1}-й элемент: ")) for i in range(8)]

# Замена элементов меньше 15 их удвоенными значениями
for i in range(8):
    if array[i] < 15:
        array[i] *= 2

print("Преобразованный массив:", array)
